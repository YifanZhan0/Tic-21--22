package MiCodigo2;

public class HefenciaFiliar {

	
	private int edad;
	private int altura;
	private int clase;
	
	public HefenciaFiliar(int edad, int altura, int clase) {
		super();
		this.edad = edad;
		this.altura = altura;
		this.clase = clase;
	}

	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}

	public int getAltura() {
		return altura;
	}

	public void setAltura(int altura) {
		this.altura = altura;
	}

	public int getClase() {
		return clase;
	}

	public void setClase(int clase) {
		this.clase = clase;
	}
	
	
	
	
	
	
}
